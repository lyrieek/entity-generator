package com.lyrieek.eg.gradle;

public class PluginParam {

	private String dbConfigPath;

	private String redInkPath;

	public String getDbConfigPath() {
		return dbConfigPath;
	}

	public void setDbConfigPath(String dbConfigPath) {
		this.dbConfigPath = dbConfigPath;
	}

	public String getRedInkPath() {
		return redInkPath;
	}

	public void setRedInkPath(String redInkPath) {
		this.redInkPath = redInkPath;
	}
}
